
public class day_of_week {
    public static void main(String args[])
    {
        int weekday = 5;
        boolean holiday = false;

        if(weekday <= 5 && !holiday)
        {
            System.out.println("Wake up at 7:00am!");
        }
        else
        {
            System.out.println("Sleep in!");
        }          
        
    }
}